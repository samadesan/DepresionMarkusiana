<h1 class="display-2 fw-bold"><span class="elementos-resaltados">Markusaiano</span><br>Crespo García</h1>
<br><hr><br>
<p class="lead mt-4 manifesto-text">
    "<span class="elementos-resaltados">Depresión Sonora</span>
    es el proyecto de <strong><span class="elementos-resaltados">Marcos Crespo</span></strong>,
    surgido en el confinamiento de 2020..."
</p><br><hr><br>
<p class="contenido-parrafos">
    <strong><span class="elementos-resaltados">Marcos Crespo</span></strong> nació en Vallecas en <strong>1997</strong>.
    El apunte biográfico no es capricho: las temáticas de sus canciones exploran las <u>inquietudes de un
        veinteañero</u> de extrarradio en su cruce por las <u>turbulencias emocionales</u> de la etapa vital que
    afronta. En el apartado musical, se nutre del <span class="elementos-resaltados"><strong>post-punk</strong></span>
    originado en décadas pretéritas, pero su discurso radiografía un presente que es el suyo y el de toda una generación <i>fatigada</i>, <i>ansiosa</i>
    y <i>empujada sin alternativa</i> a la <strong>hiperproductividad</strong>. Sus letras guardan un
    valor documental, testimonio de su propio tiempo. Si no has crecido en este contexto de dominio
    digital y recesiones económicas, tal vez te cueste un poco más entenderlo, pero lo vas a poder
    disfrutar igual. <strong>Depresión Sonora</strong> tramita el pasado y el presente para sonar contemporáneo y necesario.
</p><br>
<p class="contenido-parrafos">
    Los orígenes del proyecto se remontan a <span class="elementos-resaltados">2020</span>, año fatídico
    en el que <strong>Marcos</strong>, encerrado en su cuarto, hizo de todo mientras ahí fuera no
    pasaba nada. La coyuntura determinó el marcado carácter introspectivo de los cinco cortes que
    componían su ya mítico <span class="elementos-resaltados"><strong>EP</strong></span> homónimo.
    Aquel debut, publicado por cuenta propia primero y recuperado en formato físico por
    <a href="https://sonidomuchacho.com/" class="ds-links"><strong>Sonido Muchacho</strong></a> meses
    después, nos revelaba las virtudes que más adelante confirmaría en sucesivos lanzamientos. La
    pegada y el acierto melódico de canciones como
    <a href="https://open.spotify.com/track/2VyMyqR2ona9yAFk7WsJNW" class="ds-links"><strong>Ya no hay verano</strong></a>
    evidenciaban que estábamos ante un <span class="elementos-resaltados">diamante en bruto</span>. Era
    cuestión de tiempo que el resto del planeta reparara en él.
</p><br>
<p class="contenido-parrafos">
    Un poco más tarde, en <span class="elementos-resaltados">2021</span>, llegaba
    <a href="https://open.spotify.com/intl-es/album/1aZHSQui8o38kk6GImuqED" class="ds-links"><strong>Historias tristes para dormir bien</strong></a>,
    <span class="elementos-resaltados"><strong>EP</strong></span> de cinco temas que
    prolongaba su estado de gracia compositivo y confirmaba a Depresión Sonora como una sensación
    a ambos lados del Atlántico. Esta obra sirvió para apuntalar un sonido de vocación <strong>lo-fi</strong>, dominado
    por correosas <u>cajas de ritmos y martilleantes líneas de bajo</u>. Aciertos como
    <a href="https://open.spotify.com/intl-es/track/22S8uf8Mcg37NMH9SLrzbO" class="ds-links"><strong>Apocalipsis virtual</strong></a>,
    <a href="https://open.spotify.com/intl-es/track/648639ImUdRqgV1nzvXYQE" class="ds-links"><strong>Tú no me tienes que salvar</strong></a>
    o <a href="https://open.spotify.com/intl-es/track/6Kp3LtqAnckwX74tGwhtyJ" class="ds-links"><strong>Gasolina y mechero</strong></a>
    fueron combustible para propulsar las primeras giras por <strong>Latinoamérica</strong> y
    <strong>España</strong>, colgando el cartel de no hay billetes allá por donde pasaban.
</p><br>
<p class="contenido-parrafos">
    Con la publicación de su disco de debut en <span class="elementos-resaltados">2022</span> llegó la
    consagración definitiva.
    <a href="https://open.spotify.com/intl-es/album/2gG6tiHAfxVwPxPe6GL86p" class="ds-links"><strong>El arte de morir muy despacio</strong></a>
    se puede entender como el reverso <strong>nihilista</strong> y <strong>sarcástico</strong> de esos
    manuales de autoayuda a los que <span class="elementos-resaltados">Marcos</span> y los de su edad
    han sido sobreexpuestos. Se trata de un álbum conceptual dividido en <u>tres partes</u>, abordando
    en cada una de ellas distintas etapas vitales.
    <a href="https://open.spotify.com/intl-es/track/601n77mPApVH3bio3wcrMD" class="ds-links"><strong>Parte I: Introducción a la entropía</strong></a>
    transmite cierta inocencia no exenta de <u>mala leche</u>. El segundo
    bloque de canciones, encabezado por
    <a href="https://open.spotify.com/intl-es/track/3ISUlpOn3F8BnAYcBwhpv4" class="ds-links"><strong>Parte II: La abrazo con fuerza (carta a la soledad)</strong></a>,
    tiene un sonido más <u>saturado y subyugante</u>. Con el final de la obra llega la aceptación,
    <a href="https://open.spotify.com/intl-es/track/6xTG426uzRV9lxueq4KJGa" class="ds-links"><strong></strong>Parte III: Muerte y resurrección</a>.
    Así concluye una crónica que, de tan personal que resulta,
    <strong><span class="elementos-resaltados">acaba interpelando a una generación entera</span></strong>.
</p><br>
<p class="contenido-parrafos">
    Tras esa <strong>esperadísima</strong> ópera prima, <span class="elementos-resaltados">Marcos</span>
    no se ha dormido en los laureles. Más bien al contrario,
    se ha sumergido en una efervescente etapa de conciertos y <span class="elementos-resaltados">nueva música</span>.
    Han llegado colaboraciones con artistas como
    <a href="https://open.spotify.com/intl-es/artist/0UTzLuwz9RvFOCnwAZjUxn" class="ds-links"><strong>Bratty</strong></a>,
    <a href="https://open.spotify.com/intl-es/artist/04mTej6RpWzBxGwhfThpIi" class="ds-links"><strong>Rojuu</strong></a>,
    <a href="https://open.spotify.com/intl-es/artist/2XNJ1dXD5AjaHZTSM7qPlH" class="ds-links"><strong>Margarita Siempre Viva</strong></a>,
    <a href="https://open.spotify.com/intl-es/artist/2BVE7Pqd8kxv9xEsuvoJoE" class="ds-links"><strong>Carolina Durante</strong></a>,
    <a href="https://open.spotify.com/intl-es/artist/6c0qylj1D1gqcUUN2P8Ofp" class="ds-links"><strong>Javiera Mena</strong></a>,
    <a href="https://open.spotify.com/intl-es/artist/1jr0mAgIhKtTeKTZwk0HjF" class="ds-links"><strong>Los Punsetes</strong></a>,
    <a href="https://open.spotify.com/intl-es/artist/0N1TIXCk9Q9JbEPXQDclEL" class="ds-links"><strong>Los Planetas</strong></a> o
    <a href="https://open.spotify.com/intl-es/artist/5QS9NAK4AgJPTcRe472pZA" class="ds-links"><strong>BB Trickz</strong></a>
    y se ha gestado un nuevo <span class="elementos-resaltados"><strong>EP</strong></span>,
    <a href="https://open.spotify.com/intl-es/album/1fEmyEtKLCZyuZLaVZ6yL2" class="ds-links"><strong>MAKINAVAJA</strong></a>, que vió la luz a
    principios de <span class="elementos-resaltados">2024</span> y que supone su candidatura a ser una
    <span class="elementos-resaltados"><strong><u>estrella del pop</u></strong></span>.
    Canciones que pugnan entre la <span class="elementos-resaltados">luz</span>
    y la <span class="elementos-resaltados">oscuridad</span>, la
    <span class="elementos-resaltados">melodía</span> y el riesgo
    artístico. Parámetros en los que <strong><span class="elementos-resaltados">Depresión Sonora</span></strong>
    se mueve sin concesiones sobre lo que se espera de él o de artistas como él.
    Ocurre al contrario:
    <span class="elementos-resaltados">
                        la generación de músicos que encabeza se mueve con sus propias reglas y sus innegociables concepciones artísticas y emocionales
                    </span>. El público no ha hecho más que aplaudir su acusada personalidad y puntería creativa,
    llenando las actuaciones más grandes de su carrera en <strong>España</strong>, <strong>México</strong>
    y <strong>Estados Unidos</strong> y confirmando presencia en festivales de la talla de
    <a href="https://www.coachella.com/" class="ds-links"><strong>Coachella</strong></a>,
    <a href="https://www.primaverasound.com/es" class="ds-links"><strong>Primavera Sound</strong></a> y
    <a href="https://vivelatino.es/" class="ds-links"><strong>Vive Latino</strong></a>. Una carrera en crecimiento constante
    que no parece tener fin y que comienza ahora su etapa más
    <span class="elementos-resaltados"><strong>apasionante</strong>.
</p><br>
<p class="contenido-parrafos">
    Esta nueva etapa comienza con sus últimos lanzamientos “
    <a href="https://open.spotify.com/intl-es/track/6R8nmiJ51HOhCPNvZNLxz3" class="ds-links"><strong>La Balada de los Perros</strong></a>” y
    <a href="https://open.spotify.com/intl-es/track/0uuLlGQsuQP2kXOgpbCe0C" class="ds-links"><strong>Me Va la Vida en Esto</strong></a>, adelantos de
    su disco <a href="https://open.spotify.com/intl-es/album/54ioPa0kudt4A9VPR0K3a2" class="ds-links"><strong>Los Perros No Entienden Internet (...Y Yo no Entiendo de Sentimientos)</strong></a>.
    En este trabajo, <span class="elementos-resaltados">Marcos</span> se mueve con la lógica del mismo
    chaval que reventó Internet durante la pandemia: la de quien arriesga sin calcular, no porque no le
    importe perder, sino porque entiende que <span class="elementos-resaltados">el juego es también una forma de resistencia</span>.
    Y aunque remite a los orígenes de <strong><span class="elementos-resaltados">Depresión Sonora</span></strong>,
    el título del álbum da un <strong>giro de 180 grados</strong>: el artista que creció en los
    márgenes de la <span class="elementos-resaltados">digitalidad</span> homenajea ahora a quienes no se
    reconocen a través de una <span class="elementos-resaltados">pantalla</span>.
</p><br>
<p class="contenido-parrafos">
    <span class=""><strong><span class="elementos-resaltados">Depresión Sonora</span></strong>: La banda sonora de una generación.</span>

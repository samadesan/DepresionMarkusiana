<h1 class="display-2 fw-bold"><span class="elementos-resaltados">Introducción</span><br>a los perros</h1>
<br><hr><br>
<p class="contenido-parrafos">
    Hace años que ronda por mi cabeza la frase “Los Perros no Entienden Internet”. La apunté en la
    pizarra que tenía en mi cuarto cuando todavía vivía con mi madre. No ha sido hasta ahora, dando
    forma al disco, que he conseguido descifrar lo que realmente significaba.
</p><br>
<p class="contenido-parrafos">
    Estas canciones han estado atascadas tres años. No tengo claro si echarle la culpa a esta vida
    frenética o a que tiendo a olvidar para seguir adelante. Hace cuatro meses comencé a entender
    cuando decidí enfrentarme a la tan evitada terapia.
</p><br>
<p class="contenido-parrafos">
    Desde que tengo memoria hay voz aguda y en otro idioma que anida en mi cabeza. Siempre encuentro
    una excusa o el mundo la encuentra por mí para que la ignore y siga preocupándome por lo que sucede
    a mi alrededor (muuuuy muy lejos) y nunca dentro. Pero incluso en los momentos más fríos y racionales,
    siempre ha estado susurrándome al oído.
</p><br>
<p class="contenido-parrafos">
    Depresión Sonora surgió en un intento de escupirla, lanzarla hacia afuera. Una maraña de palabras e
    ideas que pensaba que entendía, pero con el tiempo me he dado cuenta que estaba equivocado. Qué
    difícil es aceptar que en el fondo nunca me atreví a ser honesto, ni en mi día a día, ni en mis
    letras. Que no hablé de forma sincera. ¿Por qué me sentía incomprendido? Me alegraba ver cómo esa
    enredadera os abrazaba a vosotros, pero no era capaz de paliar esa sensación constante de querer
    escapar, de que faltaba una pieza.
</p><br>
<p class="contenido-parrafos">
    Una cosa que poca gente sabe es que soy daltónico. También que de pequeño fui a una escuela de
    música y me hicieron repetir varias veces hasta que lo dejé. Siempre he pensado que mi falta de
    oído hace que todo lo que me sucede ahora sea una farsa. Aunque supongo que algo bueno tiene
    que haber en mí si he conseguido todo esto, ¿no?
</p><br>
<p class="contenido-parrafos">
    Internet y la tecnología siempre han sido mi refugio, un lugar donde sentirme seguro y que me ha
    enseñado gran parte de lo que sé. Con el tiempo, todo el aprendizaje y el entretenimiento que
    encontraba en internet desaparecieron para dar paso a la disociación, a descuidarme. Pasaba las
    horas encapsulando todo lo importante para lanzarlo bien lejos con un simple desliz de dedos. Al
    final del día, el único recuerdo que me quedaba era un collage absurdo de imágenes y sonidos de
    gente que no conocía. Y aunque el mensaje nunca fuera del todo nítido, el resultado siempre era el
    mismo: pérdida de memoria y ansiedad ante el fracaso. El miedo a que otra persona más lo estuviera
    haciendo mejor ahí fuera. Percibirme feo, tonto e inútil. Nunca he soportado sentirme insuficiente.
</p><br>
<p class="contenido-parrafos">
    Pero he tomado una decisión. No quiero que me roben la memoria, quiero construir recuerdos en base
    a lo bueno y lo malo. Me gusta pensar que empiezo a hacer caso a esa vocecita. Estoy yendo a clases
    para entender ese idioma que me había sido negado. Y así, despacito, voy deshaciendo los nudos para
    guardarlos uno a uno con cariño. Ya no quiero huir ni refugiarme, hay muchas cosas buenas que hacen
    que merezca la pena.
</p><br>
<p class="contenido-parrafos">
    Así que entre todo este caos y descubrimiento, a mis 27 años, miro hacia abajo y veo a mi perro
    Lucas, que lleva más de 13 años conmigo. Tiene artritis y algo de ceguera, pero en sus ojos negro
    café todavía veo esa inocencia que tenía cuando cabía en la palma de mi mano. Si se lo propone,
    todo le es ajeno y no parece tener preocupaciones, pero si algo le pasa por la cabeza es capaz de
    expresarlo sin complejos. Todo es sencillo. Si está feliz disfruta al máximo y si está triste llora
    un rato. Sueña, come y juega, pero también ladra enfadado y se queda inquieto al lado de la puerta.
    Tengo que aprender de mi perro, que no entiende inglés ni español, ni internet ni nada, pero que lo
    tiene claro y pasa sus días tranquilo.
</p><br>
<p class="contenido-parrafos">
    Ojalá ser como él. Ojalá poder derrumbar esos muros de mi propio castillo de cartón. Y es que no
    hace demasiado pensaba que no había nada más allá. Que la vida se sostenía tras las normas del come
    y calla, siéntate y trabaja, no llames demasiado la atención y no molestes. Pienso tanto en cómo nos
    condicionamos por nuestra educación…
</p><br>
<p class="contenido-parrafos">
    Ha llegado el momento de dejar de sobrevivir, pero como cualquier materia nueva que uno empieza a
    estudiar en profundidad, lleva tiempo. Necesito ser paciente, desprenderme de todo aquello que me
    desvíe del camino. Vencer la velocidad, aprender a escuchar. Hacer caso a mi cabezonería y a esos
    momentos de lucidez cuando creo que las cosas pueden ser de verdad.
</p><br>
<p class="contenido-parrafos">
    Está ahí, en el centro, se mueve como un mar de tormenta desde el estómago hacia los pies y la
    cabeza. Si consigo ser como Lucas, algún día podré sacar y entender todos estos sentimientos.
</p><br>

<p class="contenido-parrafos">Marcos. <br> Viernes, 1 de Noviembre de 2024. Día de todos los santos.</p>